# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, só existe duas respostas possíveis:
# sim (True) ou não (False).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que verifica a igualdade entre 2 valores.
print(10 == 10) # Sim => True (Verdadeiro)
print(10 == 11) # Não => False (Falso)
print(type(10 == 10))